/*Faça um programa que leia um número inteiro e depois o imprima usando o operador “%f”. Veja o que aconteceu.*/
#include <stdio.h>

int main(void)
{
    int x = 150;

    printf("\nValor lido com tipo de saida float: %f\n\n", x);
    printf("Valor lido com tipo de saida int: %i", x);
    return 0;
}