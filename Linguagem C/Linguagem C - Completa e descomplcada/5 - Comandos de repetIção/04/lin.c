/* Faça um programa que determine e mostre os cinco primeiros múltiplos de 3 considerando números maiores que 0. */
#include <stdio.h>
#include <math.h>

/* Incio do código */int main(void)
{
    /* declaração do código */
    int i;

    /* entrada de dados */
    for(i = 0; i <= 5; i++)
    {
        printf("%d\t", i * 3);
    }
    /* bloco de comando */
    return 0;
}
/* Fim do código */