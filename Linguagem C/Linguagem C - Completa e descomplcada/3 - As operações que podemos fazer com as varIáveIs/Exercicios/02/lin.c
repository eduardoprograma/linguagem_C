/*Faça um programa que leia um número real e imprima a quinta parte desse número.*/
#include <stdio.h>
#include <stdlib.h>

int main(void)
{
    float x = 10.5, resultado = 0;
    resultado = 10.5 * 1 / 5;
    printf("\nO valor de entrada %.2f, sua quinta parte %.2f\n\n", x, resultado);
    system("pause");
    return 0;
}